# Diferencia entre listas y arrays
# Listas son más flexibles, pero menos eficientes
# Arrays son más eficientes, pero menos flexibles
# Listas pueden contener diferentes tipos de datos
# Arrays deben contener el mismo tipo de datos
# Listas son más fáciles de usar, pero menos eficientes
# Arrays son más difíciles de usar, pero más eficientes
import array as arr
matriz = arr.array('i',[1,2,3,4,5])

for ar in matriz:
    print(ar)


